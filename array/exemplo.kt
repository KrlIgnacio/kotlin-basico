//declarando array
fun main(){
    //atribuindo elementos a um array
    val vetor: Array<Int> = arrayOf(1, 2, 3, 7, 9)

	val firstElement = vetor[0] //acessando o 1° elemento
	val thirdElement = vetor[2] //acessando o 3° elemento
    
    println(firstElement) //imprimindo 1
    println(thirdElement) //imprimindo 3
    println(vetor[4]) //imprimindo 9
}
 